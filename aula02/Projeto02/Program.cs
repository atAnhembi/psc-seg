// cria uma variável chamada idade, que pode armazenar um número inteiro
int idade;

// guarda o valor 12 dentro da variável
idade = 12;

Console.WriteLine(idade);

// soma 1 na variável idade, e armazena o resultado na própria variável
// idade = idade + 1;

idade++;

idade += 5; // idade = idade + 5;

Console.WriteLine(idade);

